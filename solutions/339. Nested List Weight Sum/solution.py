class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        
        def sumup(l,d):
            if l.isInteger():
                return l.getInteger() * d
            
            s = 0
            for e in l.getList():
                s += sumup(e,d+1)
            return s
        
        s = 0
        for e in nestedList:
            s += sumup(e,1)
        return s
    