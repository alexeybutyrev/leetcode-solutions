# Problem: Copy List with Random Pointer
# Language: python
# Runtime: 58 ms
# Memory: 15.5 MB

"""
# Definition for a Node.
class Node:
    def __init__(self, x, next=None, random=None):
        self.val = int(x)
        self.next = next
        self.random = random
"""

class Solution(object):
    def copyRandomList(self, head):
        """
        :type head: Node
        :rtype: Node
        """
        return deepcopy(head)